def create_dataset():
    os.makedirs(os.path.dirname(os.path.abspath(__file__)), exist_ok=True)
    real_df = pd.DataFrame({'text': REAL_NEWS, 'label': 'REAL'})
    fake_df = pd.DataFrame({'text': FAKE_NEWS, 'label': 'FAKE'})
    df = pd.concat([real_df, fake_df], ignore_index=True)
    df = df.sample(frac=1, random_state=42).reset_index(drop=True)

    output_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'sample_data.csv')
    df.to_csv(output_path, index=False)
    print(f"Dataset created: {len(df)} samples ({len(real_df)} real, {len(fake_df)} fake)")
    print(f"Saved to: {output_path}")

if __name__ == '__main__':
    create_dataset()
